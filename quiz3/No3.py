#Source code untuk penjumlahan
bilangan1 = input ("Bilangan yang ingin di tambah =")
bilangan2 = input ("Bilangan yang ingin di tambah =")
Penjumlahan = int (bilangan1) + int (bilangan2)
print ("Hasil Penjumlahan =",Penjumlahan)

#Source code untuk pengurangan
bilangan3 = input ("bilangan yang ingin di kurang =")
bilangan4 = input ("bilangan yang ingin di kurang =")
Pengurangan = int (bilangan3) - int (bilangan4)
print ("Hasil Pengurangan =",Pengurangan)

#Source code untuk perkalian
bilangan5 = input ("bilangan yang ingin di kalikan =")
bilangan6 = input ("bilangan yang ingin di kalikan =")
Perkalian = int (bilangan5) * int (bilangan6)
print ("Hasil Perkalian =",Perkalian)

#Source code untuk pembagian
bilangan7 = input ("bilangan yang ingin di bagi =")
bilangan8 = input ("bilangan yang ingin di bagi =")
